    echo "<br>";
    //Operasi perkalian
    $perkalian = $Angka1 * $Angka2;
    echo "Perkalian ".$Angka1." * ".$Angka2. " = ".$perkalian;
    echo "<br>";
    //Operasi pembagian
    $pembagian = $Angka1 / $Angka2;
    echo "Pembagian ".$Angka1." / ".$Angka2. " = ".$pembagian;
